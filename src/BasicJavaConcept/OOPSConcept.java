package BasicJavaConcept;

public class OOPSConcept {

   /*
    1. Object => Object means real world entity. each object has state and behaviours.
            Example: Dog
            State: Color, breed,
            Behaviours: Bark
    2. Class: Collection of objects called class.
    3. Inheritance: When an object acquires all the properties and behaviors of a parent object, it is known as inheritance.
    4. Polymorphism: If one task is performed in different ways, it is known as polymorphism.
    5. Abstraction: Hiding internal details and showing functionality is known as abstraction.
    6. Encapsulation: Binding (or wrapping) code and data together into a single unit are known as encapsulation.
    */
}
