package OOPSConcepts.Encapsulation;

public class BasicConceptOfEncapsulation {
    /*
    Encapsulation: Encapsulation in Java is a process of wrapping code and data together into a single unit.
    Encapsulation used to access the private data members in another class through getter and setter method.
    By adding only setter method you can only read the class.
    By adding only getter method you can only write the class.
    Encapsulation can be applied for only private data member of the class.
    */
}
