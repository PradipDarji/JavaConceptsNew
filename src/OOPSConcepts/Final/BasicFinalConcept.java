package OOPSConcepts.Final;

public class BasicFinalConcept {
    /*
    Final is keyword. Which can be used for variable, method and class.
    1. When any variable declare as a final then we can't change the value of that final variable.
    2. When any method declare as a final then you cannot override it.
    3. When any class declare as a final then you cannot extend it.
    */
}
