package OOPSConcepts.Interface;

public class BasicConceptOfInterface {
    /*
    An interface in Java is blueprint of class.
    The interface in Java is a mechanism to achieve abstraction. There can be only abstract methods in the Java interface,
    not method body. It is used to achieve abstraction and multiple inheritance in Java.
    1) Not able to create object of Interface
    2) Not able to create constructor of Interface
    3) In Interface variables are by default public,Static and final
    4) In Interface all methods are by default public
    5) In Interface all methods are not contain with implementation
    6) In Interface multiple inheritance is possible
    7) Class To Interface => Implement
    8) Class To Class => Extend
    9) Interface To Interface => Extend
    */
}
