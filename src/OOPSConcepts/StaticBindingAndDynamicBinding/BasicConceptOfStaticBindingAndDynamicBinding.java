package OOPSConcepts.StaticBindingAndDynamicBinding;

public class BasicConceptOfStaticBindingAndDynamicBinding {
    /*
    Static Binding or Early Binding => When type of the object is determined at compiled time(by the compiler),
    it is known as static binding.
    If there is any private, final or static method in a class, there is static binding.

    Dynamic Binding or Late Binding => When type of the object is determined at runt-time,
    it is known as dynamic binding.
    */
}
